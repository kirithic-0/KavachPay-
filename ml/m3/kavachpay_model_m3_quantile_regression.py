def winkler_score(y, lower, upper, alpha=0.20):
    width = upper - lower
    miss_low = (2/alpha) * np.maximum(0, lower - y)
    miss_high = (2/alpha) * np.maximum(0, y - upper)
    return np.mean(width + miss_low + miss_high)


def pinball(q, y_true, y_pred):
    e = y_true - y_pred
    return np.mean(np.where(e >= 0, q*e, (q-1)*e))

def make_objective(q, X_tr, y_tr_log):
    def objective(trial):
        params = {
            "objective": "quantile", "alpha": q, "metric": "quantile",
            "verbosity": -1, "boosting_type": "gbdt",
            "n_estimators": trial.suggest_int("n_estimators", 300, 3000),
            "learning_rate": trial.suggest_float("learning_rate", 0.003, 0.15, log=True),
            "num_leaves": trial.suggest_int("num_leaves", 20, 256),
            "max_depth": trial.suggest_int("max_depth", 3, 10),
            "min_child_samples": trial.suggest_int("min_child_samples", 5, 100),
            "reg_alpha": trial.suggest_float("reg_alpha", 1e-5, 10.0, log=True),
            "reg_lambda": trial.suggest_float("reg_lambda", 1e-5, 10.0, log=True),
            "min_split_gain": trial.suggest_float("min_split_gain", 0.0, 3.0),
            "subsample": trial.suggest_float("subsample", 0.5, 1.0),
            "colsample_bytree": trial.suggest_float("colsample_bytree", 0.4, 1.0),
        }
        kf = KFold(n_splits=N_SPLITS, shuffle=True, random_state=42)
        losses = []
        for tr_idx, val_idx in kf.split(X_tr):
            Xt, yt = X_tr.iloc[tr_idx], y_tr_log.iloc[tr_idx]
            Xv, yv = X_tr.iloc[val_idx], y_tr_log.iloc[val_idx]
            m = lgb.LGBMRegressor(**params)
            m.fit(Xt, yt, eval_set=[(Xv, yv)],
                  callbacks=[lgb.early_stopping(EARLY_STOP, verbose=False),
                             lgb.log_evaluation(-1)])
            pred_raw = np.maximum(0, np.expm1(m.predict(Xv)))
            y_raw = np.expm1(yv.values)
            losses.append(pinball(q, y_raw, pred_raw))
        return np.mean(losses)
    return objective

